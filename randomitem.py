import random
# player gets a random item


def random_item():
    items = ['hat', 'shirt', 'pants', 'shoes']
    chosen_item = random.choice(items)
    random_item = chosen_item
    if random_item == 'hat':
        return random_item
    elif random_item == 'shirt':
        return random_item
    elif random_item == 'pants':
        return random_item
    elif random_item == 'shoes':
        return random_item






print(random_item())